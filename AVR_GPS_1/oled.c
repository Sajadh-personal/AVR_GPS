void OLED_big_number(int page, int col, char *chr_ptr)
{
	
	
	char     c = *chr_ptr;
	int      i;
	uint8_t  data;
	int      index = c - 46;
	
	if(c < 46 || c > 58) return;
	
	    for(i = 0; i < 12; i++)
	    {
		    data = pgm_read_byte(&BigFont_12x16[index][i]);
		    OLED_Data(data);
	    }

	    // ---- Lower Page ----
	    //OLED_SetCursor(current_page + 1, current_column);
		OLED_Command(0xB0 + (page+1));
		OLED_Command(0x00 + ((col) & 0x0f));
		OLED_Command(0x10 + (((col) >> 4) & 0xf));

	    for(i = 12; i < 24; i++)
	    {
		    data = pgm_read_byte(&BigFont_12x16[index][i]);
		    OLED_Data(data);
	    }

	    //current_column += 12;   // Move to next character position
	    OLED_Command(0xB0 + (page));
	    OLED_Command(0x00 + ((col+14) & 0x0f));
	    OLED_Command(0x10 + (((col+14) >> 4) & 0xf));
}	
void OLED_char_print(char *chr_ptr)
{
	char c = *chr_ptr;
    if(c < 32 || c > 127) return;

    for(uint8_t i=0; i<5; i++)
    {
	    OLED_Data(font_5x7[c - 32][i]);
    }
    OLED_Data(0x00);  // spacing column
    
}

void OLED_write(int page, int col, char* msg)
{
	OLED_Command(0xB0 + page);
	OLED_Command(0x00 + (col & 0x0f));
	OLED_Command(0x10 + ((col >> 4) & 0xf));
	
	while(*msg != '\0')
	{
		OLED_char_print(msg++);
	}
}


void OLED_write_big(int page, int col, char* msg)
{
	OLED_Command(0xB0 + page);
	OLED_Command(0x00 + (col & 0x0f));
	OLED_Command(0x10 + ((col >> 4) & 0xf));
	
	while(*msg != '\0')
	{
		OLED_big_number(page, col, msg++);
		col+= 14;
	}
}