#define    OLED_ADDR    0x3c

void OLED_Command(uint8_t cmd)
{
	I2C_Start();
	I2C_Write(OLED_ADDR << 1); // SLA+W           <-  011 11000
	I2C_Write(0x00);           // Command mode
	I2C_Write(cmd);
	I2C_Stop();
}

void OLED_Init(void)
{
	_delay_ms(100);

	OLED_Command(0xAE); // Display OFF
	OLED_Command(0xD5); OLED_Command(0x80); // Clock
	OLED_Command(0xA8); OLED_Command(0x1F); // Multiplex 1/32
	OLED_Command(0xD3); OLED_Command(0x00); // Offset
	OLED_Command(0x40);                     // Start line

	OLED_Command(0x8D); OLED_Command(0x14); // Charge pump ON
	OLED_Command(0x20); OLED_Command(0x00); // Horizontal addressing
	OLED_Command(0xA1);                     // Segment remap
	OLED_Command(0xC8);                     // COM scan dir

	OLED_Command(0xDA); OLED_Command(0x02); // COM pins config
	OLED_Command(0x81); OLED_Command(0x7F); // Contrast
	OLED_Command(0xD9); OLED_Command(0xF1); // Pre-charge
	OLED_Command(0xDB); OLED_Command(0x40); // VCOM detect
	OLED_Command(0xA4);                     // Resume RAM display
	OLED_Command(0xA6);                     // Normal display
	OLED_Command(0xAF);                     // Display ON
}

void OLED_Data(uint8_t data)
{
	I2C_Start();
	I2C_Write(OLED_ADDR << 1); // SLA+W
	I2C_Write(0x40);           // Data mode
	I2C_Write(data);
	I2C_Stop();
}

void OLED_Clear(void)
{
	for (uint8_t page = 0; page < 4; page++)
	{
		OLED_Command(0xB0 + page);
		OLED_Command(0x00);
		OLED_Command(0x10);

		for (uint8_t col = 0; col < 128; col++)
		OLED_Data(0x00);
	}
}


